package org.kapott.hbci.GV_Result;


/**
 * Rückgabedaten für das Einreichen einer SEPA-Instant Überweisung.
 */
public class GVRInstUebSEPA extends HBCIJobResultImpl {
    private String orderId;
    private String orderStatus;
    private String cancellationCode;

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getOrderStatus() {
        return orderStatus;
    }

    public void setOrderStatus(String orderStatus) {
        this.orderStatus = orderStatus;
    }

    public String getCancellationCode() {
        return cancellationCode;
    }

    public void setCancellationCode(String cancellationCode) {
        this.cancellationCode = cancellationCode;
    }

    public String toString() {
        return String.format("GVRInstUebSepa{orderId=%s, orderStatus=%s, cancellationCode=%s}",
                orderId, orderStatus, cancellationCode);
    }
}
